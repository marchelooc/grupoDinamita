import pytest
from src.utils.logger_config import logger 
from src.api_infinity_chess.E2E_tutor import  crear_tutor, obtener_tutor, actualizar_tutor_espacios, eliminar_tutor

@pytest.mark.functional
def test_RPL058_actualizar_apellido_nombre_espacios_principio_final (get_url):
    logger.info("Iniciando test RPL058.")
    logger.info("Crear nuevo tutor.")
    cod_tutor = crear_tutor(get_url)
    logger.info("Obtener tutor creado.")
    tutor = obtener_tutor(get_url, cod_tutor)
    logger.info("Editar el nombre y apellido del tutor con espacios antes y despues del texto.")
    tutor_actualizado = actualizar_tutor_espacios(get_url,tutor[0])
    logger.info("Eliminar tutor.")
    eliminar_tutor(get_url,tutor_actualizado.get("CODTUTOR"))
    logger.info("Obtener tutor creado.")
    tutor = obtener_tutor(get_url, cod_tutor)
    logger.info("Test completado.")